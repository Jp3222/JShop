package com.jshop.controllers;

import java.awt.event.ActionListener;
import java.awt.event.KeyEvent;
import java.awt.event.KeyListener;
import java.awt.event.MouseEvent;
import java.awt.event.MouseListener;
import javax.swing.JFrame;
import javax.swing.JOptionPane;

/**
 *
 * @author juan-campos
 */
public abstract class Controller implements ActionListener, KeyListener, MouseListener {

    protected void defaultCase(JFrame view, String command, String title) {
        if (title == null) {
            title = "Comando inexistente";
        }
        JOptionPane.showMessageDialog(view,
                "El comando %s no esta disponible".formatted(command),
                title,
                JOptionPane.WARNING_MESSAGE
        );
    }
        
    @Override
    public void keyTyped(KeyEvent ke) {
    }

    @Override
    public void keyPressed(KeyEvent ke) {
    }

    @Override
    public void keyReleased(KeyEvent ke) {
    }

    @Override
    public void mouseClicked(MouseEvent me) {
    }

    @Override
    public void mousePressed(MouseEvent me) {
    }

    @Override
    public void mouseReleased(MouseEvent me) {
    }

    @Override
    public void mouseEntered(MouseEvent me) {
    }

    @Override
    public void mouseExited(MouseEvent me) {
    }

}
